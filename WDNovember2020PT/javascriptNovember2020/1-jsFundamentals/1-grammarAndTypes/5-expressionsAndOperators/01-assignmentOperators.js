//Assignment Operator
let x = 20;

//addition assignment operator
x += 1; //x=x+1
console.log(x);

//subtraction assignment operator
x -= 1;
console.log(x);

//multiplication assignment operator
x *= 5; //x = x*2
console.log(x);

//division assignment operator
x /= 4; //x = x / 4
console.log(x);

//remainder (modulus) assignment operator

x %= 2
console.log(x);

//modulus operator
console.log(50%3);
console.log(25%7); // 25/7  ---- 3 4/7

//exponential assignment operator
x **= 2;
console.log(x);


let y = 7;
y **= 3;
console.log(y);

